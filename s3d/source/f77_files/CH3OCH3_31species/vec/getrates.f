      SUBROUTINE GETRATES_I (P,T,VL, Y, DIFF, DT, ICKWRK, RCKWRK, WDOT)
      USE chemkin_m, only : MAXVL
      IMPLICIT DOUBLE PRECISION (A-H, O-Z), INTEGER (I-N)
      DIMENSION Y(MAXVL,*),WDOT(MAXVL,*),ICKWRK(*),RCKWRK(*)
      DIMENSION RF(MAXVL,175),RB(MAXVL,175),RKLOW(MAXVL,11),C(MAXVL,31)
      DIMENSION P(MAXVL),T(MAXVL)
      DIMENSION DIFF(MAXVL,*)
        INTEGER VL
      DIMENSION XQ(MAXVL,9)

